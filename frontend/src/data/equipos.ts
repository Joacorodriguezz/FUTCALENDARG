export function getLogoByName(nombre: string): string {
  const key = nombre.toLowerCase().trim();
  const logo = LOGO_MAP[key];
  if (logo) return getLogoUrl(logo);

  // Fallback: check EQUIPOS array
  const eq = EQUIPOS.find(
    (e) => e.nombre.toLowerCase() === key
  );
  return eq ? getLogoUrl(eq.logo) : '';
}

